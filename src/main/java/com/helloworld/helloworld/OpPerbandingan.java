package com.helloworld.helloworld;

public class OpPerbandingan {
    public static void main(String[] args) {
        // operasi perbandingan
        int value1 = 100;
        int value2 = 100;

        System.out.println(value1 > value2);
        System.out.println(value1 < value2);
        System.out.println(value1 >= value2);
        System.out.println(value1 <= value2);
        System.out.println(value1 == value2);
        System.out.println(value1 != value2);
    }
}
